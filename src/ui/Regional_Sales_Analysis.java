    private void pie_chart(){
    DefaultPieDataset pieDataset = new DefaultPieDataset();

        try {
            Connection con = database.db.getConnection();
            Statement st = con.createStatement();
            
            String[] regions = { "Matara", "Colombo", "Kalutara","Galle"};

            for (String region : regions) {
                String query = "SELECT SUM(price) AS Total FROM product WHERE region='" + region + "'";
                ResultSet rs = st.executeQuery(query);
                if (rs.next()) {
                    double total = rs.getDouble("Total");
                    pieDataset.setValue(region, total);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

// Create Pie Chart
        JFreeChart pieChart = ChartFactory.createPieChart(
                "Regional Sales Performance",
                pieDataset,
                true, 
                true, 
                false 
        );

// Display the chart 
        ChartFrame pieFrame = new ChartFrame("Regional Sales Performance", pieChart);
        pieFrame.setSize(600, 650);
        pieFrame.setVisible(true);
    }
    
    //bar chart
    private void bar_chart(){
     DefaultCategoryDataset dataset = new DefaultCategoryDataset();

        try {
            Connection con = database.db.getConnection();
            Statement st = con.createStatement();

            // List of regions
            String[] regions = { "Matara", "Colombo", "Kalutara","Galle"};

            for (String region : regions) {
          
                String query = "SELECT COALESCE(SUM(price), 0) AS Total FROM product WHERE region='" + region + "'";
                ResultSet rs = st.executeQuery(query);
                double total = 0;
                if (rs.next()) {
                    total = rs.getDouble("Total");
                }
                dataset.addValue(total, "product", region); 
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        // Create Bar Chart
        JFreeChart chart = ChartFactory.createBarChart(
                "Regional Sales Performance", // Chart title
                "Region", //x
                "Total Sales (LKR)", //y
                dataset
        );

        // Optional: customize chart appearance
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(java.awt.Color.BLACK);

        // Display the chart
        ChartFrame frame = new ChartFrame("Regional Sales Bar Chart", chart);
        frame.setSize(700, 500);
        frame.setVisible(true);
    }
    
    
