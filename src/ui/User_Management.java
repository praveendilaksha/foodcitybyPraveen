    public ArrayList<user> user_list() {
        ArrayList<user> user_list = new ArrayList<>();
        try {
            Connection con;
            Statement st;
            con = database.db.getConnection();
            st = con.createStatement();
            String query = "Select * from user";
            ResultSet rs = st.executeQuery(query);
            user users;
            while (rs.next()) {

                users = new user(rs.getInt("user_id"), rs.getString("user_name"), rs.getString("password"), rs.getString("role"));
                user_list.add(users);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return user_list;
    }

    public void show_user_data() {
        ArrayList<user> list =user_list();
        DefaultTableModel model = (DefaultTableModel) tbl_user.getModel();
        Object[] row = new Object[4];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getUser_id();
            row[1] = list.get(i).getUser_name();
            row[2] = list.get(i).getPassword();
            row[3] = list.get(i).getRole();
          
            model.addRow(row);
        }

    }

    private void print_data() {
        try {
            boolean complete = tbl_user.print(JTable.PrintMode.FIT_WIDTH,
                    new MessageFormat("Datas"),
                    new MessageFormat("Page {0}"));
            if (complete) {
                JOptionPane.showMessageDialog(null, "Complete");
            } else {
                JOptionPane.showMessageDialog(null, "Not Complete");
            }
        } catch (PrinterException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

