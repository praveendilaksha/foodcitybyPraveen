private void add_user(){
    try {
            Connection con;
            PreparedStatement pst;
            con = database.db.getConnection();
            String user_id = txt_user_id.getText();
            String username = txt_user_name2.getText();
            String password = txt_password.getText();
            String role = jComboBox1.getSelectedItem().toString();

            // Validation
            if (username.isEmpty() ||user_id.isEmpty() || password.isEmpty() || role.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill all text box");
                return;
            }

            // Insert new user into database
            String query = "INSERT INTO user (user_id,user_name, password, role) VALUES (?,?, ?, ?)";
            pst = con.prepareStatement(query);
            pst.setString(1, user_id);
            pst.setString(2, username);
            pst.setString(3, password);
            pst.setString(4, role);

            int row = pst.executeUpdate();

            if (row > 0) {
                JOptionPane.showMessageDialog(null, "User Added Successfully!");

                // Refresh table after insert
                DefaultTableModel model = (DefaultTableModel) tbl_user.getModel();
                model.setRowCount(0);
                show_user_data();

                // Clear text fields
            txt_password.setText("");
            txt_user_id.setText("");
            txt_user_name2.setText("");
            jComboBox1.setSelectedIndex(0);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
}

