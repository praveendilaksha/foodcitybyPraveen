    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String username = txt_username.getText();
        String password = txt_password.getText();
        String role = jComboBox1.getSelectedItem().toString(); 
        try {
            Connection con;
            PreparedStatement pst;
            
            con = database.db.getConnection();
                    
            String query = "SELECT user_name, password FROM user WHERE user_name = ? AND password = ? AND role = ?";
            pst = con.prepareStatement(query);
            pst.setString(1, username);
            pst.setString(2, password);
            pst.setString(3, role);
            ResultSet rs = pst.executeQuery();
            if (rs.next()){
                Main_Application md = new Main_Application();
                md.setVisible(true);
                this.dispose();
            }else{
              JOptionPane.showMessageDialog(null, "Invalid User Name or Password and Role ");
            }
                    
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

